import axios from "axios"

export default {
    async getItems (apiServer, apiTable) {
        console.log('????????????????????????????????????????')
        return  await axios.get(`${apiServer}${apiTable}`)
    },
    async saveItems(apiServer, apiTable, data){
        if(apiTable === 'promotion_medicines'){
            data = { promotion_id: data.promotion_id, medicine_id: data.medicine_id}
        } else if (apiTable === 'invoice_medicines') {
            data = { invoice_id: data.invoice_id, medicine_id: data.medicine_id}
        }
        console.log('????????????????????????????????????????', )
        console.log(data)
        return await axios.post(`${apiServer}${apiTable}`, data)
    },
    async editItems(apiServer, apiTable, data){
        console.log('????????????????????????????????????????')
        console.log(data)
        return await axios.put(`${apiServer}${apiTable}`, data)
    },
    async deleteItems(apiServer, apiTable, id){
        console.log('????????????????????????????????????????')
        return await axios.delete(`${apiServer}${apiTable}/${id}`)
    },
}